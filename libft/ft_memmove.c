#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char *strsrc;
	char *strdst;

	strsrc = (char*)src;
	strdst = (char*)dst;
	if (strsrc < strdst)
	{
		strsrc = strsrc + len - 1;
		strdst = strdst + len - 1;
		while (len--)
			*strdst-- = *strsrc--;
	}
	else
		while (len--)
			*strdst++ = *strsrc++;
	return (dst);
}
