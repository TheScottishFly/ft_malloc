#include "ft_printf.h"

void	ft_lstdelone(t_ptf **s)
{
	if (s && *s)
	{
		if ((*s)->arg)
			ft_strdel(&(*s)->arg);
		if ((*s)->warg)
			free((*s)->warg);
		if ((*s)->option)
			ft_strdel(&(*s)->option);
	}
}
