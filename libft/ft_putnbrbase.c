#include "libft.h"

void	ft_putnbrbase(uintmax_t nbr, char *base, size_t baselen)
{
	if (nbr >= baselen)
	{
		ft_putnbrbase(nbr / baselen, base, baselen);
		ft_putnbrbase(nbr % baselen, base, baselen);
	}
	else
		ft_putchar(base[nbr]);
}
