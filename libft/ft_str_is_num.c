int		ft_str_is_num(char *str)
{
	int	i;

	i = 0;
	if (!str)
		return (0);
	while (str[i] != '\0')
	{
		if (!(str[0] >= '0' && str[0] <= '9'))
			return (0);
		i++;
	}
	return (1);
}
