#include "libft.h"

int	ft_atoi(const char *str)
{
	int sign;
	int nbr;
	int i;

	sign = 0;
	nbr = 0;
	i = 0;
	while ((str[i] >= 9) && (str[i] <= 13 || str[i] == ' '))
		i++;
	if (str[i] == '-')
		sign = 1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = nbr * 10;
		nbr = nbr + str[i] - '0';
		i++;
	}
	if (sign)
		return (-nbr);
	else
		return (nbr);
}
