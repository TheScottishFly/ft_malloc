#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*strcdst;
	const char	*strsrc;
	int			i;

	i = 0;
	strsrc = src;
	strcdst = dst;
	while (n--)
	{
		strcdst[i] = strsrc[i];
		i++;
	}
	return (dst);
}
