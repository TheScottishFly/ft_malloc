#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char *strdst;

	strdst = dst;
	while (*src != '\0' && len > 0)
	{
		*strdst++ = *src++;
		len--;
	}
	while (len > 0)
	{
		*strdst++ = '\0';
		len--;
	}
	return (dst);
}
